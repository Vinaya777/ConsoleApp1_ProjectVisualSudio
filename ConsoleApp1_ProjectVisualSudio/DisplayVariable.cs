using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp1_ProjectVisualSudio
{
    class DisplayVariable
    {
        static void Main(String[]args)
        {
            byte b = 32;
            Console.WriteLine("Byte Value : " + b);

            int a = 14;
            Console.WriteLine("Int Value : " + a);

            float f = 2.30f;
            Console.WriteLine("Float Vale : " + f);

            double d = 1452.254;
            Console.WriteLine("Double Value : " + d);

            bool bresult = true;
            Console.WriteLine("Boolean Value : " + bresult);

            short s = 2;
            Console.WriteLine("Short Value : " + s);

            long l = 2345678;
            Console.WriteLine("Long Value : " + l);



        }
    }
}
