using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp1_ProjectVisualSudio.LoopBasic
{
    class ProgramtoDisplay
    {
        static void Main(string[] args)
        {
            for (int i = 8; i >= 4;)
            {
                Console.WriteLine(i);
                i--;
            }
        }
    }
}